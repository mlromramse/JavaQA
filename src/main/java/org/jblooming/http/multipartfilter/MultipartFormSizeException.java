package org.jblooming.http.multipartfilter;

public class MultipartFormSizeException extends javax.servlet.ServletException {
  MultipartFormSizeException() {
    super();
  }

  MultipartFormSizeException(String message) {
    super(message);
  }

  MultipartFormSizeException(Throwable t) {
    super(t);
  }

  MultipartFormSizeException(String message, Throwable t) {
    super(message, t);
  }
}

